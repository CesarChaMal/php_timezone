DROP TABLE IF EXISTS `paises`;
CREATE TABLE IF NOT EXISTS `paises` (
  `codpais` varchar(3) NOT NULL default '',
  `pais` varchar(30) default NULL,
  PRIMARY KEY  (`codpais`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

DROP TABLE IF EXISTS `timezone`;
