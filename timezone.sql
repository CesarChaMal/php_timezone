  `timezone` varchar(50) default NULL COMMENT 'Valor de la variable global TZ en PHP que cambia la zona horaria del servidor',
  `descripcion` varchar(100) NOT NULL,
  `activo` binary(1) NOT NULL default '1',
  PRIMARY KEY  (`timezoneid`)
